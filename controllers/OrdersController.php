    public function actionListKota($province_id)
    {
        $kota = Cities::find()->where(['province_id' => $province_id])->all();
        foreach ($kota as $data) {
            echo "<option value=" .$data['city_id']. ">".$data['city_name']."</option>";
        }
    }
}
